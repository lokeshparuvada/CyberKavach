def translate(key: str, lang: str = "en", **kwargs) -> str:
    """Translate a fixed UI string by key, with optional .format(**kwargs)."""
    lang = lang if lang in SUPPORTED_LANGUAGES else "en"
    table = STRINGS.get(key, {})
    text = table.get(lang) or table.get("en") or key
    return text.format(**kwargs) if kwargs else text


def translate_signal(signal_id: str, lang: str = "en") -> str:
    lang = lang if lang in SUPPORTED_LANGUAGES else "en"
    table = SIGNAL_EXPLANATIONS.get(signal_id, {})
    return table.get(lang) or table.get("en") or signal_id.replace("_", " ")


